    void mostrarClientes(){
        tabla.addColumn("ID Cliente");
        tabla.addColumn("Nombre");
        tabla.addColumn("Apellido Paterno");
        tabla.addColumn("Apellido Materno");
        tabla.addColumn("Telefono");
        tabla.addColumn("Email");
        tabla.addColumn("Saldo");
        tabla.addColumn("Rut Empresa");
        
        try{
            Connection con = conec.obtenerConexion();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select id_cliente, nombre_cli, apaterno_cli, amaterno_cli, fono_cli, email_cli, saldo_cli, empresa_rut_empresa from cliente");
            
            String datos[] = new String[8];
            
            while(rs.next()){
                
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                
                tabla.addRow(datos);
            }
            tablaClientes.setModel(tabla);
                    
                    
        }catch (SQLException ex){
            java.util.logging.Logger.getLogger(ClientesTodos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
